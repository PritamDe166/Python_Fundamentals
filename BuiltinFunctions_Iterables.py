# # len() - length
print(len([4, 5, 1, 3]))

# sorted() - Sorting
nums1 = [6, 5, 8, 3, 2, 9, 11, 14, 10]
print(sorted(nums1))
print(sorted(nums1, reverse=True))
print(nums1)

# reversed() - Reverse Iterator
nums2 = [50, 10, 40, 20]
print(reversed(nums2))
print(list(reversed(nums2)))

name1 = "Pritam"
print(list(reversed(name1)))